<h1>Welcome to Undressed MVC</h1>
<h2>A PHP MySQLi framework</h2>
<p>
The 'Undressed MVC' framework is a PHP MVC framework buit with simplicity, speed and readibility in mind. No ORM witchcraft, nor any absurd stuff.
</p>
<p>
The 'Undressed MVC' is meant to be easily understood by any PHP programmer with some understanding of PHP. 
</p>
<p>
There's no obfuscation nor any other techniques to make the framework code harder to read.
</p>
<p>
The 'Undressed MVC' handles <b>routing</b> for you (so you don't have to create your own regular expressions, however feel free to check the Router class if you would like to understand how it works), <b>form validation</b>, helps you make your <b>database</b> calls (works for mysqli, if you'd like to change that, feel free to change the DB class. There is <b>no ORM</b>. You just call a method from the DB class and the argument is your SQL query. This way you don't have to write the same repetatives lines of code each time you need to get some data, while not being limited by ORMs. This way you can have outer joins, unions and subqueries easily, as long as you have some SQL knowledge.) among other features.
</p>